    public void abrirItemMenu(ItemsMenuPpal itemMenuPpal) {
        Component comps[] = this.panPrincipal.getComponents();
        int i = 0;
        while(comps != null && i < comps.length) {
            this.panPrincipal.remove(comps[i]);
            i++;
        }
        this.panPrincipal.revalidate();
        this.panPrincipal.repaint();
        
        switch (itemMenuPpal) {
            case RIPS_CARGAR:
                FrmCargarRIPS frmCargaRIPS = new FrmCargarRIPS();
                this.panPrincipal.add(frmCargaRIPS);
                frmCargaRIPS.setVisible(true);
                break;
            case RIPS_ADMINISTRAR:
                FrmAdministrarRIPS frmAdministrarRIPS = new FrmAdministrarRIPS(this);
                this.panPrincipal.add(frmAdministrarRIPS);
                frmAdministrarRIPS.setVisible(true);
                break;
            case ACERCA_DE:
                FrmAcercaDe frmAcercaDe = new FrmAcercaDe();
                this.panPrincipal.add(frmAcercaDe);
                frmAcercaDe.setVisible(true);
                break;
            default:
                JOptionPane.showMessageDialog(this, "VALOR DE OPCION DESCONOCIDO");
                break;
        }
    }
    
